package day27fungsi;
        // Ini adalah program Java sederhana tentang funsi
public class Main {
    // Ini adalah fungsi utama
    public static void main(String[] args) {
        // Memanggil fungsi sapa()
        sapa("Rendiawan");
    }

    // Ini adalah fungsi sapa()
    // Fungsi ini menerima satu parameter dan mencetak pesan sapaan
    public static void sapa(String nama) {
        System.out.println("Halo, " + nama + "! Selamat datang di kampus universitas Sulawesi Barat.");
    }
}