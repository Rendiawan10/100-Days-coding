package pkg100_days_of_coding;
public class day78OperasiLogika {
    public static void main(String[] args) {
        boolean nilai1 = true;
        boolean nilai2 = false;
        
        boolean hasilAnd = nilai1 && nilai2;
        System.out.println("Hasil AND: " + hasilAnd);
        
        boolean hasilOr = nilai1 || nilai2;
        System.out.println("Hasil OR: " + hasilOr);
        
        boolean hasilNot = !nilai1;
        System.out.println("Hasil NOT: " + hasilNot);
    }
    
}
